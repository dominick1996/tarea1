<center><h3><font color="white">6. Los zombies del sífilis en las calles de Italia:</p></h3><font color="white"></center>



En 1494 Italia no se parecía en nada a la que conocemos hoy en día. Un enorme brote de sífilis asolaba la ciudad, y
 la enfermedad no era escondida con vergüenza, como sucedería años más tarde, sino que se sufría frente a todo el mundo.

La enfermedad causaba que la piel en el rostro de las personas se cayera, as&iacute; como las extremidades y los genitales. 
En términos estéticos, era prácticamente como vivir en una película de zombies. Las personas morían en el correr de 
algunos meses, con su piel completamente destruida.
