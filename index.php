<center><h3><font color="white">5. Herculano y el Vesubio:</p></h3><font color="white"></center>


Cuando pensamos en el Vesubio automáticamente pensamos en Pompeya, quizás por haber sido una de las ciudades
 más importantes y esta ahora enterrada debajo de la ceniza. Sin embargo, Herculano fue una ciudad pequeña, 
 mucho más cercana al Vesubio y con consecuencias peores. Se dice que incluso algunos cráneos explotaron debido 
 al calor concentrado y el magma cayendo en la ciudad. Estar allí durante la erupción seguro fue un infierno, 
 con todos los elementos del Inframundo.
