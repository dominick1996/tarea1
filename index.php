<td><center><h3><font color="blue">
<center><h3><font color="white">3. Chucky realmente existió:</p></h3><font color="white"></center>
La película Child’s Play, donde un niño tiene un muñeco con un espíritu maligno dentro, est&aacute; basada en una historia 
real. Robert es un muñeco de tamaño real que pertenecía al niño Gene Otto. Al parecer, un sirviente se enojó con la 
familia y puso un hechizo sobre el muñeco. El niño comenzó a hablar con él y para sorpresa de todos, recibía respuesta. 
Sin embargo, pocos creyeron en lo que Gene contaba sobre que el muñeco movía objetos. Luego de su muerte, Robert pasó de
 mano en mano y ahora está en un museo donde, según cuentan, suceden cosas extrañas.

