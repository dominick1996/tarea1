<center><h3><font color="white">7. El hombre que intentó salvar a Lincoln:</p></h3><font color="white"></center>


Aunque sabemos exactamente qué pasó cuando Abraham Lincoln fue asesinado, y también conocemos el
 nombre del asesino, poco sabemos del hombre que intentó salvar al presidente. Henry Rathbone estaba
  allí con su esposa cuando falló en su cometido: no pudo salvar a Lincoln y esto cambió su vida radicalmente.

Rathbone nunca se recuperó mentalmente e intent&oacute; matar a sus tres hijos 18 años más tarde. Cuando 
su esposa lo impidió, la apuñaló y luego intentó suicidarse. Cuando la policía lo encontró, el hombre 
seguía repitiendo que había personas escondidas detrás de los cuadros. Vivió el resto de su vida en un asilo, 
convencido de que alguien intentaba envenenarlo a través de las paredes.
