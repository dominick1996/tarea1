<center><h3><font color="white">4. La primera asesina serial:</p></h3><font color="white"></center>
Aunque no lo creas, el primer registro de alguien que asesinara gente en grandes cantidades fue una mujer en Roma
 llamada Locusta, precisamente en un período pacífico de la ciudad. En el siglo I fue encarcelada por envenenamiento, 
 pero Agripina le dio el perdón luego de que la ayudara a asesinar al Emperador Claudius. Lo mismo sucedió tiempo después 
 con Nerón, quien además le dio un sitio para enseñar sus artes y convertirse en profesora. Sin embargo, no pudo salvarse 
 cuando el Emperador Galba la condenó y fue ejecutada públicamente.

