<td><center><h3><font color="blue">
<center><h3><font color="white">2. La canción que mata personas:</p></h3><font color="white"></center>
En los años 30 se popularizó la canción Gloomy Sunday, pero poco a poco fue tornándose terrorífica
 cuando se difundió que más de 18 personas (algunos dicen incluso 100) se suicidaron en Budapest debido
  a ella. Hungría siempre tuvo altas tasas de suicidio, y la canción era tan popular que no resulta extraño
   que un suicida la estuviera escuchando. Además, la letra depresiva no ayuda demasiado para mejorar el humor.
    Sin embargo, hubo un suicidio real por ella: el creador de la canción se suicidó luego de que su único éxito 
    fuera culpado de estas calamidades.
